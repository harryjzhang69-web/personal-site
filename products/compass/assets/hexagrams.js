// 通过六位二进制找到对应卦
function findHexagram(binaryStr) {
  return HEXAGRAMS.find(h => h.binary === binaryStr) || HEXAGRAMS[0];
}

// 随机起卦（模拟摇钱）
function randomCast() {
  let bin = "";
  for (let i = 0; i < 6; i++) {
    bin += Math.random() < 0.5 ? "0" : "1";
  }
  return findHexagram(bin);
}

// 数字起卦（用户输入两个数）
function numberCast(upper, lower) {
  // 上卦：upper % 8，下卦：lower % 8
  const TRIGRAMS = ["111", "011", "101", "001", "110", "010", "100", "000"]; // 乾兑离震巽坎艮坤
  const upTri = TRIGRAMS[upper % 8];
  const downTri = TRIGRAMS[lower % 8];
  return findHexagram(downTri + upTri);
}

// 时间起卦（用当前时间）
function timeCast() {
  const now = new Date();
  return numberCast(now.getMonth() + now.getDate(), now.getHours());
}

